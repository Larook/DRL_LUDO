def map_enemy_tile_id_to_player_0(i_enemy, tile_id):
    """ knowing enemy id and the tile that he sees
     check what is the tile in the coordinates of ai player """
    tile_ai = i_enemy*13 + tile_id
    if tile_ai >= 53:
        tile_ai -= 53
        tile_ai += 1
    if tile_id >= 54:
        tile_ai = 0
    return tile_ai


